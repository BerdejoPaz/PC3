//Clase Adicion con metodos sobrecargados
class Adicion {
    // Método para sumar dos números
    public int sumar(int num1, int num2) {
        return num1 + num2;
    }

    // Método para sumar tres números
    public int sumar(int num1, int num2, int num3) {
        return num1 + num2 + num3;
    }

    // Método para sumar cuatro números
    public int sumar(int num1, int num2, int num3, int num4) {
        return num1 + num2 + num3 + num4;
    }

    // Método para sumar cinco números
    public int sumar(int num1, int num2, int num3, int num4, int num5) {
        return num1 + num2 + num3 + num4 + num5;
    }
}